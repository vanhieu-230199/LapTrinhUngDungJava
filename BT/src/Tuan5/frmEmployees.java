    public void showEmployees(){
        Employees employees = list.get(list.size()-1);
        model.addRow(new Object[]{
            employees.getId() , 
            employees.getName(),
            employees.getAddress(),
            employees.getGender(),
            employees.getKnowledge(),
            employees.getSubject(),
        });
    }
       
    
