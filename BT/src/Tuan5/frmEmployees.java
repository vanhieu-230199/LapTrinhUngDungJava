    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        Employees employees = new Employees();
        employees.setId(Integer.parseInt(txtId.getText()));
        employees.setName(txtName.getText());
        employees.setAddress(txtAddress.getText());

         if (radiMale.isSelected()) {
            employees.setGender("nam");

        } else {
            employees.setGender("nu");
        }
        
          if (chkJava.isSelected() && chkPython.isSelected()) {
            employees.setKnowledge("Java + Python");

        } else if(chkJava.isSelected()){
           employees.setKnowledge("Java");
        } else if(chkPython.isSelected()){
           employees.setKnowledge("Python");
        }
          employees.setSubject(txtSub.getText());
         
         if( new DBConnection().addEmployees(employees)){
             JOptionPane.showMessageDialog(rootPane, "Luu thanh cong");
             list.add(employees);
             showEmployees();
         } else {
             JOptionPane.showMessageDialog(rootPane, "Faill");
         }   
    }//GEN-LAST:event_btnSaveActionPerformed
        
